def _merge_dep(acc: Dict[str, str], name: str, version: Any, source: Path) -> None:
    """Merge a single dep into the accumulator. Conflicts emit a warning and keep the first-seen pin."""
    if not isinstance(version, str):
        echo(
            f"warning: {source}: '{name}' has non-string version spec "
            f"({type(version).__name__}); skipping. Tabular pins aren't supported by gen-pixi.",
            "yellow",
        )
        return
    if name in acc and acc[name] != version:
        echo(
            f"warning: '{name}' pinned to both '{acc[name]}' and '{version}'; "
            f"keeping '{acc[name]}' (later seen in {source})",
            "yellow",
        )
        return
    acc[name] = version


def _aggregate_package_deps(
    package_manifests: List[Path],
) -> Dict[str, str]:
    """Walk each package's pixi.toml and union host- + run-dependencies."""
    deps: Dict[str, str] = {}
    for manifest in package_manifests:
        data = toml.load(manifest)
        pkg = data.get("package", {})
        for table in ("host-dependencies", "run-dependencies"):
            for name, version in pkg.get(table, {}).items():
                _merge_dep(deps, name, version, manifest)
    return deps


def _render_workspace_manifest(workspace_name: str, deps: Dict[str, str]) -> str:
    """Render a deterministic workspace pixi.toml. Deps sorted alphabetically."""
    lines = [
        "# AUTO-GENERATED by `platform ws gen-pixi`.",
        "# Edits here will be lost the next time the command runs.",
        "# Source of truth for per-package deps is each packages/*/pixi.toml.",
        "",
        "[workspace]",
        f'name = "{workspace_name}"',
        "channels = [",
        '  "http://localhost:8000",',
        '  "https://prefix.dev/robostack-kilted",',
        '  "https://prefix.dev/conda-forge",',
        "]",
        'platforms = ["linux-64"]',
        "",
        "[dependencies]",
    ]
    for name in sorted(deps):
        lines.append(f'{name} = "{deps[name]}"')
    lines.append("")
    return "\n".join(lines)


def _ensure_gitignore_entry(repo_root: Path, pattern: str) -> bool:
    """Append `pattern` to .gitignore if not already present. Returns True if the file was changed."""
    gitignore = repo_root / ".gitignore"
    existing = gitignore.read_text() if gitignore.exists() else ""
    if any(line.strip() == pattern for line in existing.splitlines()):
        return False
    if existing and not existing.endswith("\n"):
        existing += "\n"
    gitignore.write_text(existing + pattern + "\n")
    return True


def _is_single_package_repo(root_manifest: Path) -> bool:
    """A repo is single-package if its root pixi.toml declares a [package] block."""
    if not root_manifest.exists():
        return False
    return "package" in toml.load(root_manifest)


