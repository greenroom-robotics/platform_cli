def get_auth_file() -> Dict[str, str]:
    entries = (Path().home() / ".gr" / "auth").read_text().strip().split("\n")
    matches = [re.match(r"export (?P<key>\w+)=(?P<value>.+)", e) for e in entries]
    return {m.group("key"): m.group("value") for m in matches if m}


def get_system_platform_path() -> Path:
    return Path.home() / "work/platform"


def ws_symlinks_to_platform_paths(
    workspace_path: Path, packages: Dict[Any, Any]
) -> Tuple[Dict[str, Path], Dict[str, Path]]:
    platform_paths = {}
    other_paths = {}
    for path in (workspace_path / "src").iterdir():
        if path.is_symlink():
            pkg_name = path.name
            pkg_path = path.resolve()
            if (
                pkg_path.parts[: len(get_system_platform_path().parts)]
                != get_system_platform_path().parts
            ):
                echo(f"Non platform based package: {pkg_path}", "yellow")
                other_paths[pkg_name] = pkg_path
            else:
                platform_paths[pkg_name] = pkg_path
    return platform_paths, other_paths


class Workspace(PlatformCliGroup):
    def create(self, cli: click.Group):
        @cli.group(help="CLI handlers associated with colcon workspaces")
        def ws():
            pass

        @ws.command(name="gen-pixi")
        @click.option(
            "--ros-distro",
            default="kilted",
            show_default=True,
            help="ROS distro for the dev shell's `ros-<distro>-desktop` meta-package.",
        )
        @click.option(
            "--check",
            is_flag=True,
            default=False,
            help="Print the rendered manifest to stdout instead of writing.",
        )
        def gen_pixi(ros_distro: str, check: bool):  # type: ignore
            """Generate a workspace pixi.toml from each packages/*/pixi.toml.

            Aggregates [package.host-dependencies] and [package.run-dependencies]
            from each sub-package, layers in dev tooling (colcon, rosdep,
            compilers, cmake, ninja, etc.), and writes the result to the repo
            root. The generated pixi.toml is added to .gitignore so it's never
            committed.

            For single-package repos (root pixi.toml has a [package] block),
            the root manifest is hand-maintained and this command is a no-op.
            """
            cwd = Path.cwd()
            root_manifest = cwd / "pixi.toml"

            if _is_single_package_repo(root_manifest):
                echo(
                    "Single-package repo detected (root pixi.toml has [package]). "
                    "The root manifest is hand-maintained; nothing to generate.",
                    "yellow",
                )
                return

            package_manifests = sorted(cwd.glob("packages/*/pixi.toml"))
            if not package_manifests:
                echo("No packages/*/pixi.toml found.", "red")
                raise click.Abort()

            deps = _aggregate_package_deps(package_manifests)

            # Always-on dev tooling. setdefault so an explicit pin in a
            # package's pixi.toml takes precedence over our defaults.
            for name, version in {
                f"ros-{ros_distro}-desktop": "*",
                "colcon-common-extensions": "*",
                "rosdep": "*",
                "compilers": "*",
                "cmake": "*",
                "ninja": "*",
                "make": "*",
                "pkg-config": "*",
            }.items():
                deps.setdefault(name, version)

            rendered = _render_workspace_manifest(cwd.name, deps)

            if check:
                click.echo(rendered)
                return

            root_manifest.write_text(rendered)
            echo(f"Wrote {root_manifest} ({len(deps)} deps)", "green")

            if _ensure_gitignore_entry(cwd, "/pixi.toml"):
                echo("Added '/pixi.toml' to .gitignore", "green")

